package Day_1;

public class Unary_Operators {
 
	public static void main(String[] args) {
		int a = 10;
		System.out.println("a++ = " + (a++));//11 a = a+1
		System.out.println("a-- = " + (a--));//10 a = a-1
		System.out.println(a);
	}
}


// Unary Operators:- These operators require only one operand to trigger computation.
// 1. Increment operator:- a++
// 2. Decrement Operator:- a--
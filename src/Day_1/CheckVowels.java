package Day_1;

public class CheckVowels {

	public static void main(String[] args) {
		// a,i,e,u,o   or A,I,O,E,U
		char ch = 'o';
		switch(ch) {
		case 'a':{
			System.out.println("vowel");
			break;
		}
		case 'i':{
			System.out.println("vowel");
			break;
		}
		case 'e':{
			System.out.println("vowel");
			break;
		}
		case 'u':{
			System.out.println("vowel");
			break;
		}
		case 'o':{
			System.out.println("vowel");
			break;
		}
		case 'A':{
			System.out.println("vowel");
			break;
		}
		case 'I':{
			System.out.println("vowel");
			break;
		}
		case 'E':{
			System.out.println("vowel");
			break;
		}
		case 'U':{
			System.out.println("vowel");
			break;
		}
		case 'O':{
			System.out.println("vowel");
			break;
		}
		default:{
			System.out.println("not vowel");
		}
		}

	}

}

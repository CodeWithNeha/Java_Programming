package Day_1;

public class Relational_Operators {

	public static void main(String args[]) {
		// ==, >, <, >=, <=, !=
		int a = 12;
		int b = 4;
		// ==
		System.out.println(" a == b  " + (a==b));   // False
		System.out.println(" a > b  " + (a>b));     // True
		System.out.println(" a < b  " + (a<b));     // False
		System.out.println(" a >= b  " + (a>=b));   // True
		System.out.println(" a <= b  " + (a<=b));   // False
		System.out.println(" a != b  " + (a!=b));   // True
		
	}
}

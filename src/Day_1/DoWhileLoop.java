package Day_1;

public class DoWhileLoop {

	public static void main(String[] args) {
		// Do While loop :- Exit Control Loop
		//  1. initialization
		//  2. test condition
		//  3. inc/dec
		//  initialization
		//  do
		//  {
		// 		statement-block;
		//       inc/dec;
		//  }
		//  while(test condition);
		int i = 1;   // initialization
		do {
			System.out.println(i);
			i++;    // inc/dec
		}
		while(i<10);     //test condition
		
	}

}
